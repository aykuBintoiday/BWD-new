// Hàm random lấy n phần tử từ 1 mảng
function layNgauNhienTuMang(arr, n) {
  const shuffled = arr.slice().sort(() => 0.5 - Math.random());
  return shuffled.slice(0, n);
}

// Gợi ý địa điểm nhập từ người dùng
function goiYDiaDiem() {
  const input = document.getElementById("searchDiaDiem").value.toLowerCase();
  const ketQua = document.getElementById("goiyKetQua");
  ketQua.innerHTML = "";

  if (input.trim() === "") return;

  const goiy = Object.keys(duLichData).filter(d => d.toLowerCase().includes(input));
  goiy.forEach(item => {
    const li = document.createElement("li");
    li.textContent = item;
    li.onclick = () => {
      document.getElementById("searchDiaDiem").value = item;
      ketQua.innerHTML = "";
    };
    ketQua.appendChild(li);
  });
}

// Xử lý và hiển thị kết quả gợi ý
function hienThongBao() {
  const diaDiem = document.getElementById("searchDiaDiem").value.trim();
  const ngayText = document.getElementById("soNgayDi").value.trim(); // Lấy lại số ngày người dùng nhập

  const popup = document.getElementById("popupGoiy");
  const overlay = document.getElementById("overlay");
  const popupNoiDung = document.getElementById("popupNoiDung");

  if (!duLichData[diaDiem]) {
    popupNoiDung.innerHTML = `<p>❌ Hiện chưa có dữ liệu cho địa điểm "<strong>${diaDiem}</strong>".</p>`;
  } else {
    const data = duLichData[diaDiem];
    const diemThamQuan = layNgauNhienTuMang(data.diadiem, 5);
    const phuongTienNgauNhien = layNgauNhienTuMang(data.phuongtien, 1)[0];
    const noiNghiNgauNhien = layNgauNhienTuMang(data.noinghi, 1)[0];
    const loiChucNgauNhien = thongDiepXanh[Math.floor(Math.random() * thongDiepXanh.length)];

    popupNoiDung.innerHTML = `
      <h3>🎯 Gợi ý cho bạn: ${diaDiem}</h3>
      <p><strong>📅 Số ngày đi:</strong> ${ngayText}</p>
      <h4>📌 Địa điểm bạn nên đi:</h4>
      <ul>${diemThamQuan.map(diem => `<li>${diem}</li>`).join("")}</ul>
      <p><strong>🏡 Nơi nghỉ gợi ý:</strong> ${noiNghiNgauNhien}</p>
      <p style="margin-top: 20px; font-style: italic;">🌱 ${loiChucNgauNhien}</p>
      <div style="margin-top: 30px; display: flex; justify-content: center; gap: 20px;">
       <button class="popup-button" onclick="hienThongBao()">Gợi ý khác</button>
      </div>
    `;
  }

  overlay.style.display = "block";
  popup.style.display = "block";
  setTimeout(() => {
    popup.classList.add("show");
    overlay.style.opacity = 1;
  }, 10);
}

function dongThongBao() {
  const popup = document.getElementById("popupGoiy");
  const overlay = document.getElementById("overlay");
  
  popup.classList.remove("show");
  overlay.style.opacity = 0;

  setTimeout(() => {
    popup.style.display = "none";
    overlay.style.display = "none";
  }, 500); // match with transition time
}

window.addEventListener("DOMContentLoaded", function () {
  const observer = new IntersectionObserver(
    function (entries) {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("active");
        } else {
          entry.target.classList.remove("active");
        }
      });
    },
    {
      threshold: 0.3, // Khi 30% div hiển thị thì bắt đầu hiện
    }
  );

  const section = document.querySelector(".gthieusanpham");
  if (section) {
    observer.observe(section);
  }
});

